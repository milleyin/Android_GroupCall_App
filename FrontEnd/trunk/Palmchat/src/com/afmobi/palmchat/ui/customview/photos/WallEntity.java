package com.afmobi.palmchat.ui.customview.photos;

public class WallEntity {

	public static final byte PhotoTypePhoto = 0; // Default
	public static final byte PhotoTypeAdd = 1;
	public static final byte PhotoTypeDelete = 2;

	public String sn;
	public String afid;
	public String serverUrl;
	public int photoType = PhotoTypePhoto;

	public WallEntity(String afid, String sn, String serverUrl) {
		super();
		this.afid = afid;
		this.sn = sn;
		this.serverUrl = serverUrl;
	}

	public WallEntity(String afid, String sn, String serverUrl, int photoType) {
		super();
		this.afid = afid;
		this.sn = sn;
		this.serverUrl = serverUrl;
		this.photoType = photoType;
	}

}
