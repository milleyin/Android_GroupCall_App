package com.afmobi.palmchat.eventbusmodel;

import com.core.Consts;

public class LocalsFilterEvent {
	private byte mSex = Consts.AFMOBI_SEX_FEMALE_AND_MALE;
	private boolean mIsOnline, mIsNearby;
	public byte getSex(){
		return mSex;
	}
	public boolean isOnline(){
		return mIsOnline;
	}
	public boolean isNearBy(){
		return mIsNearby;
	}
	public LocalsFilterEvent(byte sex,boolean isOnLine,boolean isNearBy){
		mSex=sex;
		mIsOnline=isOnLine;
		mIsNearby=isNearBy;
	}
}
