package com.core;

public class AfPbInfo {
	
	public void setPbInfo(int total_count, int cur_start, int next_start, int cur_len, String name[], String phone[])
	{
		this.total_count = total_count;
		this.cur_start = cur_start;
		this.next_start = next_start;
		this.cur_len = cur_len;
		this.name = name;		
		this.phone = phone;		
	}
	
	public AfPbInfo()
	{
		
	}
	public int total_count;
	public int cur_start; 
	public int next_start;
	public int cur_len;	
	public String name[];
	public String phone[];		
}
