package com.afmobi.palmcall.model;

import java.util.Date;

public class CallRecord {
    private int id;

    private String afid;

    private String receiverafid; //拨打电话给谁的afid

    private int minutes; //消费分钟数

    private int status; //状态0打电话消费，1赠送，2充值

    private Date addtime;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getAfid() {
        return afid;
    }

    public void setAfid(String afid) {
        this.afid = afid;
    }

    public String getReceiverafid() {
        return receiverafid;
    }

    public void setReceiverafid(String receiverafid) {
        this.receiverafid = receiverafid;
    }

    public int getMinutes() {
        return minutes;
    }

    public void setMinutes(int minutes) {
        this.minutes = minutes;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public Date getAddtime() {
        return addtime;
    }

    public void setAddtime(Date addtime) {
        this.addtime = addtime;
    }

    @Override
    public String toString() {
        return "CallRecord{" +
                "id=" + id +
                ", afid='" + afid + '\'' +
                ", receiverafid='" + receiverafid + '\'' +
                ", minutes=" + minutes +
                ", status=" + status +
                ", addtime=" + addtime +
                '}';
    }

}
